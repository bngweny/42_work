#include <unistd.h>

void	rush(int h, int w)
{
	char c = 'x';
	int i=h;
	

	while(i>=0)
	{
		int j = w;
		while (j>=0)
		{
			if(i == h || i==0 ||j==w || j==0)
			{
				write(1, &c , 1);
			}
			else
			{
				write(1, "  " , 1);
			}
			j--;
		}
		write(1, "\n", 1);
		i--;
	}
}

int 	main()
{
	rush(5,10);
	return (0);
}
