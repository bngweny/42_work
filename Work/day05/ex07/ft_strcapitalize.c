char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i - 1] == ' ' || i == 0)
		{
			if ((str[i] >= 'a' && str[i] <= 'z'))
				str[i] = str[i] - 32;
		}
		else if ((str[i] >= 'A' && str[i] <= 'Z'))
		{
			str[i] = (str[i] + 32);
		}
		i++;
	}
	return (str);
}
